package day04;

public class Inequlity_sign_operator {

	public static void main(String[] args) {
		int a=10; //a변수에 10 저장
		int b=20; //b변수에 20 저장
		int c=30; //c변수에 30 저장
		
		System.out.println(a<b); //a(주어)가 b보다 작아?
		System.out.println(a>b); //a(주어)가 b보다 커?
		System.out.println(a+b<=c); //a(주어)+b가 c보다 작거나 같아?
		System.out.println(a+b>=c); //a(주어)+b가 c보다 크거나 같아?

	}

}
